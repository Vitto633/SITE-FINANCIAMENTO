function mudaCard() {
  let card = document.querySelector('.btn-card');
  let button = document.getElementById('btnText');

  if (card) {
    if(card.classList.contains('click')){
      card.classList.remove('click');
      button.innerHTML = "+";
    }else{
      card.classList.add('click');
      button.innerHTML = "-";
    }
    
  }
}

